/// `-tiff -unprocessed` skips colour-space conversion and gamma. The output
/// must be deterministic and structurally well-formed (non-empty, plausible
/// channel range — never all-zero or all-saturated).
#[test]
fn merrill_unprocessed_tiff_has_signal() {
    let input = skip_if_missing!(MERRILL_INPUT);
    let out = run_extract(
        &input,
        &["-tiff", "-no-denoise", "-unprocessed", "-no-crop"],
        ".tif",
    );
    let img = read_tiff_rgb16(&out);
    assert!(img.width > 0 && img.height > 0, "empty image");
    assert_eq!(
        img.pixels.len(),
        img.width as usize * img.height as usize * 3,
        "interleaved RGB pixel count mismatch"
    );
    let (min, max) = channel_extents(&img);
    assert!(
        max > min + 100,
        "unprocessed TIFF has near-uniform pixel values (min={min}, max={max}); RAW load likely failed"
    );
}

/// Two `-dng` runs must produce pixel-identical raw planes.
///
/// `merrill_dng_is_deterministic` (above) compares the IFD0 preview via
/// `read_tiff_rgb16` (which reads `seek_to_image(0)`); this test reaches
/// into the SubIFD raw plane to catch non-determinism in the processing
/// pipeline that wouldn't show up in the downsampled preview. Important
/// after we cut over to the Rust DNG writer — we lost the libtiff
/// baseline and rely on self-consistency for raw-plane regression
/// coverage until tier-3 goldens are committed.
#[test]
fn dng_raw_plane_is_deterministic() {
    let input = skip_if_missing!(MERRILL_REAL);
    let switches = &["-dng", "-no-denoise"];
    let a = run_extract(&input, switches, ".dng");
    let img_a = read_dng_raw_ifd(&a);
    let b = run_extract(&input, switches, ".dng");
    let img_b = read_dng_raw_ifd(&b);
    let diff = image_diff(&img_a, &img_b);
    assert_eq!(
        diff.max_abs_diff, 0,
        "DNG raw plane is non-deterministic on {MERRILL_REAL}: {diff:?}"
    );
}

/// Same as `dng_raw_plane_is_deterministic` but for a Quattro file. Exercises
/// the M5a Rust port of `x3f_expand_quattro` — the bicubic upsample uses
/// pre-computed weight tables and per-channel deinterleave/reinterleave
/// loops that would expose any nondeterminism (e.g. accidental rayon
