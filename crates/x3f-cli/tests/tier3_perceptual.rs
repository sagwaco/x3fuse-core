/// Quattro `-dng` output should have plausible pixel values across the full
/// frame after expansion. Specifically, the upsampled BMT image should not
/// be all-zero or all-saturated, and the channel range should span more
/// than ~100 LSB. Catches the case where `x3f_expand_quattro` returns
/// without populating `expanded` (the M0 stub `exit(2)`'d; a regression
/// here would be e.g. an off-by-one stride error producing rows of zeros).
///
/// `-no-crop` gives this test a different switch fingerprint from
/// `quattro_dng_raw_plane_is_deterministic` so the test harness picks a
/// distinct scratch dir and they don't race when run in parallel.
#[test]
fn quattro_dng_raw_plane_has_signal() {
    let input = skip_if_missing!(QUATTRO_REAL);
    let out = run_extract(&input, &["-dng", "-no-denoise", "-no-crop"], ".dng");
    let img = read_dng_raw_ifd(&out);
    assert!(img.width > 0 && img.height > 0);
    let (min, max) = channel_extents(&img);
    assert!(
        max > min + 100,
        "Quattro raw plane has near-uniform values (min={min}, max={max}); expand_quattro likely no-op'd"
    );
}

/// Read the DNG raw plane. Both the legacy libtiff path and the new Rust
/// writer place the raw IFD in `IFD0.SubIFDs[0]` and leave the IFD0 →
/// IFD1 chain link at 0. The `tiff` crate's `seek_to_image` walks the
/// chain link and so doesn't reach the raw plane — we walk SubIFDs by
/// hand and decode the strips ourselves. The format is always
/// uncompressed 16-bit RGB for our test invocations (no `-compress`).
fn read_dng_raw_ifd(path: &Path) -> RgbImage {
    let bytes = fs::read(path).unwrap_or_else(|e| panic!("read `{}`: {e}", path.display()));
    assert_eq!(&bytes[..2], b"II", "DNG must be little-endian");
    assert_eq!(read_u16(&bytes, 2), 42, "TIFF magic mismatch");
    let ifd0_off = read_u32(&bytes, 4) as usize;
    let raw_ifd_off = read_subifd0_offset(&bytes, ifd0_off);
    let raw_ifd = parse_ifd(&bytes, raw_ifd_off);

    let width = first_long_or_short(&bytes, &raw_ifd, 256);
    let height = first_long_or_short(&bytes, &raw_ifd, 257);
    let bits_per_sample = first_long_or_short(&bytes, &raw_ifd, 258);
    let samples_per_pixel = first_long_or_short(&bytes, &raw_ifd, 277);
    assert_eq!(bits_per_sample, 16, "raw IFD must be 16-bit");
    assert_eq!(samples_per_pixel, 3, "raw IFD must be 3-channel");
    let compression = first_long_or_short(&bytes, &raw_ifd, 259);
    assert_eq!(compression, 1, "test expects uncompressed raw plane");

    let strip_offsets = read_long_or_short_vec(&bytes, &raw_ifd, 273);
    let strip_byte_counts = read_long_or_short_vec(&bytes, &raw_ifd, 279);
    assert_eq!(strip_offsets.len(), strip_byte_counts.len());

    let mut pixels: Vec<u16> =
        Vec::with_capacity((width as usize) * (height as usize) * (samples_per_pixel as usize));
    for (off, len) in strip_offsets.iter().zip(strip_byte_counts.iter()) {
        let strip = &bytes[*off as usize..(*off as usize) + (*len as usize)];
        for chunk in strip.chunks_exact(2) {
            pixels.push(u16::from_le_bytes([chunk[0], chunk[1]]));
        }
    }
    RgbImage {
        width,
        height,
        pixels,
    }
}

fn read_u16(b: &[u8], off: usize) -> u16 {
    u16::from_le_bytes([b[off], b[off + 1]])
}
fn read_u32(b: &[u8], off: usize) -> u32 {
    u32::from_le_bytes([b[off], b[off + 1], b[off + 2], b[off + 3]])
}

#[derive(Clone)]
struct IfdEntry {
    tag: u16,
    typ: u16,
    count: u32,
    /// Either the inline 4-byte payload or the file offset of the payload.
    raw_value: [u8; 4],
}

fn parse_ifd(bytes: &[u8], off: usize) -> Vec<IfdEntry> {
    let count = read_u16(bytes, off) as usize;
    let mut entries = Vec::with_capacity(count);
    let base = off + 2;
    for i in 0..count {
        let p = base + i * 12;
        entries.push(IfdEntry {
            tag: read_u16(bytes, p),
            typ: read_u16(bytes, p + 2),
            count: read_u32(bytes, p + 4),
            raw_value: [bytes[p + 8], bytes[p + 9], bytes[p + 10], bytes[p + 11]],
        });
    }
    entries
}

fn read_subifd0_offset(bytes: &[u8], ifd0_off: usize) -> usize {
    let ifd0 = parse_ifd(bytes, ifd0_off);
    let entry = ifd0
        .iter()
        .find(|e| e.tag == 330)
        .expect("DNG IFD0 missing SubIFDs tag");
    // SubIFDs is a LONG[N]. With N=1 it fits inline.
    if entry.count == 1 {
        u32::from_le_bytes(entry.raw_value) as usize
    } else {
        let off = u32::from_le_bytes(entry.raw_value) as usize;
        read_u32(bytes, off) as usize
    }
}

fn first_long_or_short(bytes: &[u8], ifd: &[IfdEntry], tag: u16) -> u32 {
    read_long_or_short_vec(bytes, ifd, tag)[0]
}

fn read_long_or_short_vec(bytes: &[u8], ifd: &[IfdEntry], tag: u16) -> Vec<u32> {
    let entry = ifd
        .iter()
        .find(|e| e.tag == tag)
        .unwrap_or_else(|| panic!("raw IFD missing tag {tag}"));
    let element_size = match entry.typ {
        3 => 2,
        4 => 4,
        other => panic!("tag {tag} has unexpected type {other}"),
    };
    let total_size = (entry.count as usize) * element_size;
    let payload: &[u8] = if total_size <= 4 {
        &entry.raw_value[..total_size]
    } else {
        let off = u32::from_le_bytes(entry.raw_value) as usize;
        &bytes[off..off + total_size]
    };
    match entry.typ {
        3 => payload
            .chunks_exact(2)
            .map(|c| u16::from_le_bytes([c[0], c[1]]) as u32)
            .collect(),
        4 => payload
            .chunks_exact(4)
            .map(|c| u32::from_le_bytes([c[0], c[1], c[2], c[3]]))
            .collect(),
        _ => unreachable!(),
    }
}

fn channel_extents(img: &RgbImage) -> (u16, u16) {
    let mut min = u16::MAX;
    let mut max = 0u16;
    for &p in &img.pixels {
        if p < min {
            min = p;
        }
        if p > max {
            max = p;
        }
    }
    (min, max)
}
